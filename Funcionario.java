    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        int valor = JOptionPane.showConfirmDialog(null, "Confirma Salvar?", "Opções", JOptionPane.YES_NO_CANCEL_OPTION);
        
        if(valor == 0){
            // Sim
            Func f = new Func();
            f.setNome(nome.getText());
            f.setTel(tel.getText());
            f.setFunc(func.getText());
            f.setEndereco(endereco.getText());
            
            area.setText(f.status());
        }
        else if(valor == 1){
            // Não
        }
        else{
            // Cancela
        }
    }//GEN-LAST:event_saveActionPerformed

